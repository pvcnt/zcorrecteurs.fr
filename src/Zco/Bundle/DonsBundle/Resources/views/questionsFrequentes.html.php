<h2>Qui est l’association Corrigraphie et quel est son rôle ?</h2>

<p>
	Corrigraphie est une association française à but non lucratif (loi 1901)
	créée en mai 2011 pour soutenir la croissance du site zCorrecteurs.fr.
	D’un côté elle s’occupe de tous les aspects administratifs, légaux et 
	financiers liés au site. De l’autre, elle est en charge de guider son 
	développement afin qu’il puisse étendre son action.
</p>

