<h2>Partage et transfert des données</h2>

<p class="good">
	<b>Publicité.</b> La publicité affichée sur le site peut être ciblée par 
	rapport à votre âge (s’il a été renseigné), votre pays de provenance et 
	la section du site que vous visitez. Ce sont les seules informations 
	susceptibles d’être transmises à un serveur de publicité. Aucune 
	information nominative (pseudonyme, adresse courriel…) n’est transmise.
</p>

<p class="good">
	<b>Données non privées ou non personnelles.</b> Nous pouvons utiliser des 
	données résultant de statistiques, c’est-à-dire ne comportant aucune 
	information permettant d’identifier personnellement un utilisateur, ou bien 
	des données accessibles publiquement.
</p>

<p class="good">
	<b>Application de la loi.</b> Nous pouvons être amenés à communiquer certaines 
	données, y compris pour une identification ultérieure, si la demande en est 
	faite par une autorité judiciaire, de police ou habilitée par la loi.
</p>

<p class="good">
	<b>Non divulgation et transfert des données.</b> À l’exception des cas 
	ci-dessus, nous ne divulgons ni transférons aucune donnée à des personnes 
	extérieures non habilitées.
</p>

